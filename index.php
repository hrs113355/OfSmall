<h2>小的機器人 1.0</h2>
<p>目前的構想是收集兩部份的資料，一是隨機 follow plurk 言論，作為回覆來源的參考資料庫，另一部份是『小的機器人』會講的話的資料庫。利用標記輔助機器學習的方式，由人標定哪句話應該用什麼樣的內容回覆以建立連結，作為機器學習的 Training Data，再利用這些資料為基礎，用訓練出來的結果使用噗浪功能回應使用本機器人的人。</p>
